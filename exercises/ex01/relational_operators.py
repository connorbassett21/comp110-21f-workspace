left_hand_variable: int = int(input("Left-hand side: "))

right_hand_variable: int = int(input("Right-hand side: "))

print(str(left_hand_variable) + " < " + str(right_hand_variable) + " is " + str(left_hand_variable < right_hand_variable))
print(str(left_hand_variable) + " >= " + str(right_hand_variable) + " is " + str(left_hand_variable >= right_hand_variable))
print(str(left_hand_variable) + " == " + str(right_hand_variable) + " is " + str(left_hand_variable == right_hand_variable))
print(str(left_hand_variable) + " != " + str(right_hand_variable) + " is " + str(left_hand_variable != right_hand_variable))